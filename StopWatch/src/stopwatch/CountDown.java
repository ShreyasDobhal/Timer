    private void timeUpdate() {
        HH.setValue(hrs);
        MM.setValue(min);
        SS.setValue(sec);
    }
    
    private void startTimer() {
        Thread thread = new Thread(new Runnable(){
            @Override
            public void run() {
                try {
                        while (start) {
                            curr=(int)(100*frac);
                            rem+=((100*frac)-curr);
                            Thread.sleep((int)(curr+rem));
                            //System.out.println((int)(curr+rem));
                            if (rem>=1)
                                    rem--;
                            msec++;
                            if (msec>=10) {
                                msec=0;
                                sec--;
                                if (hrs<=0&&min<=0&&sec<=10)
                                    Toolkit.getDefaultToolkit().beep();
                            }
                            if (sec<0) {
                                sec=59;
                                min--;
                            }
                            if (min<0) {
                                min=59;
                                hrs--;
                            }
                            if (hrs<0) {
                                endTimer();
                            }
                            timeUpdate();
                        }
                    }
                    catch (Exception e) {
                        ;
                    }
            }
        });
        thread.start();
    }
    
    private void endTimer() {
        start=false;
        hrs=0;
        min=0;
        sec=0;
        btnStart.setEnabled(true);
        btnStop.setEnabled(false);
        JOptionPane.showMessageDialog (null,"Time Over");
        timeUpdate();
    }
    
   
