    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // TODO add your handling code here:
        try {
            hrs=Integer.valueOf(HH.getValue().toString());
            min=Integer.valueOf(MM.getValue().toString());
            sec=Integer.valueOf(SS.getValue().toString());
        }
        catch (Exception e) {
            ;
        }
        if (hrs<0) {
            min+=hrs*60;
            hrs=0;
        }
        if (min<0) {
            sec+=min*60;
            min=0;
        }
        if (sec<0) {
            sec=0;
        }
        if (sec>60) {
            min+=sec/60;
            sec=sec%60;
        }
        if (min>60) {
            hrs+=min/60;
            min=min%60;
        }
        timeUpdate();
        if (hrs==0&&min==0&&sec==0) {
            start=false;
            btnStart.setEnabled(true);
            btnStop.setEnabled(false);
            JOptionPane.showMessageDialog(null,"Set the timer first");
        }
        else {
            start=true;
            btnStart.setEnabled(false);
            btnStop.setEnabled(true);
            startTimer();
        }
        
    }//GEN-LAST:event_btnStartActionPerformed

